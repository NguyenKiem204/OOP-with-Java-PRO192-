
public class Brick {
	protected String place;
	protected int price;
	public Brick() {
		super();
	}
	public Brick(String place, int price) {
		super();
		this.place = place;
		this.price = price;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	

}
